    private void tablePedidosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePedidosMousePressed
        // TODO add your handling code here:

        TableModel tableModel = tablePedidos.getModel();
        int codigo = (int) tableModel.getValueAt(tablePedidos.getSelectedRow(), 0);
        System.out.println("El codigo del pedido es: "+ codigo);
        Peticion peticion = new Peticion(TipoAccion.CONSULTAR_PEDIDO, codigo);
        Client conexion = new Client(peticion);

        Object respuesta = conexion.getRespuestaServer();
        if(respuesta != null){
            Pedido pedidoRespuesta = (Pedido)respuesta;
            txtDesgloce.setText(pedidoRespuesta.mostrarDesgloce());
            infoPedido.setText(pedidoRespuesta.toString());
        }

    }//GEN-LAST:event_tablePedidosMousePressed

