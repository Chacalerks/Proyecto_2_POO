    public void cargarTabla() {
        ArrayList datosEntrada = new ArrayList();
        
        datosEntrada.add("catalogoAlimentos.dat");
        
        Peticion peticion = new Peticion(TipoAccion.VER_PRODUCTOS, datosEntrada);
        Client conexion = new Client(peticion);
        
        
        Object respuesta = conexion.getRespuestaServer();
        if(respuesta != null){
            tableAlimentos.setModel((DefaultTableModel)respuesta);
            tableAlimentos.setDefaultEditor(Object.class, null);
        }
        
        
        //tableSismos.setModel(VentanaPrincipal.controlador.cargarSismos());
        //tableSismos.setDefaultEditor(Object.class, null);
    }
    
    public void cargarCampos(Alimento alimento) {
        txtCodigo.setText(alimento.getCodigo());
        txtNombre.setText(alimento.getNombre());
        txtDescripcion.setText(alimento.getDescripcion());       
        txtSizePorcion.setText(String.valueOf(alimento.getSizePorcion()));
        txtPiezas.setText(String.valueOf(alimento.getPiezasPorcion()));
        txtCalorias.setText(String.valueOf(alimento.getCaloriasPorcion()));
        //caloriasPieza = Integer.parseInt(txtSizePorcion.getText());
        txtPrecio.setText(String.valueOf(alimento.getPrecio()));
        img = alimento.getImg();
        imgAlimento.setIcon(Utilities.loadResizeIcon(img, 380));
        
       cboVisibilidad.setSelectedIndex(Utilities.convertVisibleToInt(alimento.getVisibilidad()));
        
        
    }
