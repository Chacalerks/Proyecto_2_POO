    private void btnPagarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPagarMouseClicked
        // TODO add your handling code here:
        TableModel tableModel = tablePedidos.getModel();
        int codigo = (int) tableModel.getValueAt(tablePedidos.getSelectedRow(), 0);
        Peticion peticion = new Peticion(TipoAccion.ELIMINAR_PEDIDO, codigo);
        Client conexion = new Client(peticion);

        Object respuesta = conexion.getRespuestaServer();
        if(respuesta != null){
            boolean answer = (boolean)respuesta;
            if(answer){
                JOptionPane.showMessageDialog(null, "Se ha pagado correctamente el pedido!", "Listo", JOptionPane.INFORMATION_MESSAGE);
                loadDatos();
            }else
                JOptionPane.showMessageDialog(null, "Hubo un problema al pagar el pedido!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPagarMouseClicked


