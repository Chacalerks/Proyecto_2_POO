    private void imgUploadImageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_imgUploadImageMouseClicked
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.dir")));
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            img = "src\\img\\catalago\\"+selectedFile.getName();
            imgAlimento.setIcon(Utilities.loadResizeIcon(img, 380));
        }
    }//GEN-LAST:event_imgUploadImageMouseClicked

