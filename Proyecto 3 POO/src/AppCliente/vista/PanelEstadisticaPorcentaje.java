    public void loadDatos(){        
        Peticion peticion = new Peticion(TipoAccion.PORCENTAJE, "");
        Client conexion = new Client(peticion);     
        
        Object respuesta = conexion.getRespuestaServer();
        if(respuesta != null){
            ArrayList datos = (ArrayList)respuesta;
            lbSitio.setText(String.valueOf((double)datos.get(0))+" %");
            lbExpress.setText(String.valueOf((double)datos.get(1))+" %");
            lbRecoger.setText(String.valueOf((double)datos.get(2))+" %");
        }else{
            System.out.println("La vara es nul");
        }
    }

